#include "ft_boolean.h"

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

t_bool	ft_is_even(int nbr)
{
	if (IS_EVEN(nbr))
		return (TRUE);
	else
		return (FALSE);
}
